float GetTypeEffectiveness(EPokemonType MoveType, EPokemonType DefenderType1, EPokemonType DefenderType2)
{
	int32 MoveIdx = (int32)MoveType;
	float Multiplier = 1.0f;

	if (DefenderType1 != EPokemonType::None)
	{
		Multiplier *= TypeChart[MoveIdx][(int32)DefenderType1];
	}
	if (DefenderType2 != EPokemonType::None)
	{
		Multiplier *= TypeChart[MoveIdx][(int32)DefenderType2];
	}

	return Multiplier;
}

float GetSTABMultiplier(EPokemonType MoveType, EPokemonType AttackerType1, EPokemonType AttackerType2)
{
	if (MoveType == AttackerType1 || MoveType == AttackerType2)
	{
		return 1.5f;
	}
	return 1.0f;
}
